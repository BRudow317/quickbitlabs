-- =====================================================
-- QBL.QBL_USER_SESSION
-- =====================================================
CREATE TABLE qbl_user_session (
    qbl_user_session_id NUMBER GENERATED BY DEFAULT AS IDENTITY
                        CONSTRAINT PK_QBL_USER_SESSION PRIMARY KEY,
    qbl_user_id NUMBER REFERENCES qbl_users(qbl_user_id) ON DELETE CASCADE NOT NULL,
    token_hash  VARCHAR2(64 CHAR)  NOT NULL,
    issued_at   TIMESTAMP NOT NULL,
    expires_at  TIMESTAMP NOT NULL,
    is_active   NUMBER(1,0) DEFAULT 1 NOT NULL,
    ip_address  VARCHAR2(45 CHAR),
    user_agent  VARCHAR2(512 CHAR),
    session_data CLOB,
    created_at TIMESTAMP DEFAULT SYSTIMESTAMP NOT NULL,
    created_by VARCHAR2(100 CHAR) DEFAULT 'SYSTEM' NOT NULL,
    updated_at TIMESTAMP DEFAULT SYSTIMESTAMP NOT NULL,
    updated_by VARCHAR2(100 CHAR) DEFAULT 'SYSTEM' NOT NULL
);


CREATE INDEX IDX_QBL_USER_SESSION_USER_ID  ON qbl_user_session (qbl_user_id);
CREATE INDEX IDX_QBL_USER_SESSION_TOKEN    ON qbl_user_session (token_hash);
CREATE INDEX IDX_QBL_USER_SESSION_ACTIVE   ON qbl_user_session (is_active, expires_at);

-- =====================================================
-- QBL.QBL_USER_SIGN_IN
-- qbl_user_id is nullable so failed attempts for unknown usernames
-- can still be recorded (attempted_username captures the raw input).
-- =====================================================
CREATE TABLE qbl_user_sign_in (
    qbl_user_sign_in_id NUMBER GENERATED BY DEFAULT AS IDENTITY
                        CONSTRAINT PK_QBL_USER_SIGN_IN PRIMARY KEY,
    qbl_user_id       NUMBER REFERENCES qbl_users(qbl_user_id) ON DELETE CASCADE,
    attempted_username VARCHAR2(100 CHAR),
    ip_address         VARCHAR2(45 CHAR),
    user_agent         VARCHAR2(512 CHAR),
    success            NUMBER(1,0) DEFAULT 0 NOT NULL,
    failure_reason     VARCHAR2(256 CHAR),
    created_at TIMESTAMP DEFAULT SYSTIMESTAMP NOT NULL,
    created_by VARCHAR2(100 CHAR) DEFAULT 'SYSTEM' NOT NULL,
    updated_at TIMESTAMP DEFAULT SYSTIMESTAMP NOT NULL,
    updated_by VARCHAR2(100 CHAR) DEFAULT 'SYSTEM' NOT NULL
);

CREATE INDEX IDX_QBL_USER_SIGN_IN_USER_DATE ON qbl_user_sign_in (qbl_user_id, created_at);
CREATE INDEX IDX_QBL_USER_SIGN_IN_IP_DATE   ON qbl_user_sign_in (ip_address, created_at);

