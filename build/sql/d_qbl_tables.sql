CREATE TABLE qbl_catalog_shares (
    qbl_catalog_shares_id NUMBER GENERATED BY DEFAULT AS IDENTITY CONSTRAINT PK_QBL_CATALOG_SHARES PRIMARY KEY,
    catalog_id  VARCHAR2(36 CHAR) REFERENCES qbl_catalog_registry(catalog_id) ON DELETE CASCADE,
    share_scope VARCHAR2(20 CHAR) DEFAULT 'USER' REFERENCES qbl_user_roles(qbl_role) ON DELETE CASCADE,
    shared_user_id NUMBER REFERENCES qbl_users(qbl_user_id) ON DELETE CASCADE,
    shared_group_id NUMBER REFERENCES qbl_groups(qbl_groups_id) ON DELETE CASCADE,
    permission_level VARCHAR2(50 CHAR) CHECK (permission_level IN ('VIEWER', 'EDITOR')),
     -- Audit fields
     created_at TIMESTAMP DEFAULT SYSTIMESTAMP NOT NULL,
     created_by VARCHAR2(100 CHAR) DEFAULT 'SYSTEM' NOT NULL,
     updated_at TIMESTAMP DEFAULT SYSTIMESTAMP NOT NULL,
     updated_by VARCHAR2(100 CHAR) DEFAULT 'SYSTEM' NOT NULL
);
