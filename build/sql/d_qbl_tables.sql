-- =====================================================
-- QBL.QBL_CATALOG_REGISTRY
-- =====================================================
CREATE TABLE qbl_catalog_registry (
    catalog_id      VARCHAR2(36 CHAR) DEFAULT SYS_GUID() CONSTRAINT PK_QBL_CATALOG_REGISTRY PRIMARY KEY,
    name            VARCHAR2(500 CHAR) NOT NULL,
    alias           VARCHAR2(255 CHAR),
    label           VARCHAR2(255 CHAR),
    namespace       VARCHAR2(255 CHAR),
    description     VARCHAR2(1000 CHAR),
    catalog_limit   NUMBER,
    catalog_offset  NUMBER,
    entities CLOB CONSTRAINT chk_catalog_entities_json CHECK (entities    IS JSON),
    filters CLOB CONSTRAINT chk_catalog_filters_json CHECK (filters IS JSON),
    joins CLOB CONSTRAINT chk_catalog_joins_json CHECK (joins       IS JSON),
    sort_columns CLOB CONSTRAINT chk_catalog_sort_columns_json CHECK (sort_columns IS JSON),
    assignments CLOB CONSTRAINT chk_catalog_assignments_json CHECK (assignments IS JSON),
    source_type VARCHAR2(50 CHAR),
    owner_user_id NUMBER REFERENCES qbl_users(qbl_user_id) ON DELETE CASCADE,
    catalog_version NUMBER DEFAULT 1 NOT NULL,
    properties CLOB CONSTRAINT chk_catalog_properties_json  CHECK (properties IS JSON),
    created_at TIMESTAMP DEFAULT SYSTIMESTAMP NOT NULL,
    created_by VARCHAR2(100 CHAR) DEFAULT USER NOT NULL,
    updated_at TIMESTAMP DEFAULT SYSTIMESTAMP NOT NULL,
    updated_by VARCHAR2(100 CHAR) DEFAULT USER NOT NULL,

    CONSTRAINT UQ_QBL_CATALOG_REGISTRY UNIQUE (owner_user_id, name)
);


CREATE INDEX IDX_QBL_CATALOG_REGISTRY_USER ON qbl_catalog_registry (owner_user_id);
CREATE INDEX IDX_QBL_CATALOG_REGISTRY_SOURCE ON qbl_catalog_registry (source_type);


