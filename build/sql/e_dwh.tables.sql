/**************************************************************************
  Schema: DWH
  Description: Data Warehouse tables for Master Data Management (MDM)
               based on QBL.MQ_PKG logic.
  Last Edit: 2026-05-10
**************************************************************************/

-- =====================================================
-- DWH.MQ_LOOKUP
-- Centralized table for categorical labels/types.
-- Using unique integer lookup_code as PK.
-- =====================================================
CREATE TABLE dwh.mq_lookup (
  lookup_id  NUMBER CONSTRAINT PK_MQ_LOOKUP PRIMARY KEY,
  category     VARCHAR2(32 CHAR) NOT NULL,
  lookup_desc  VARCHAR2(100 CHAR) NOT NULL,
  created_at   TIMESTAMP DEFAULT ON NULL SYSTIMESTAMP NOT NULL,
  created_by   VARCHAR2(100 CHAR) DEFAULT ON NULL USER NOT NULL,
  updated_at   TIMESTAMP DEFAULT ON NULL SYSTIMESTAMP NOT NULL,
  updated_by   VARCHAR2(100 CHAR) DEFAULT ON NULL USER NOT NULL
);

-- =====================================================
-- DWH.MQ_PKG_LOG
-- Audit log written by mq_logger (AUTONOMOUS_TRANSACTION).
-- =====================================================
CREATE TABLE dwh.mq_pkg_log (
  log_id                NUMBER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  mq_id                 NUMBER,
  json_record           CLOB,
  error_code            NUMBER,
  error_message         VARCHAR2(2000 CHAR),
  error_location        VARCHAR2(4000 CHAR),
  mq_status_code        NUMBER,
  procedure_name        VARCHAR2(100 CHAR),
  procedure_description VARCHAR2(512 CHAR),
  package_name          VARCHAR2(100 CHAR),
  created_at            TIMESTAMP DEFAULT ON NULL SYSTIMESTAMP NOT NULL,
  created_by            VARCHAR2(100 CHAR) DEFAULT ON NULL USER NOT NULL
);

CREATE INDEX IDX_MQ_PKG_LOG_MQ_ID ON dwh.mq_pkg_log(mq_id);
CREATE INDEX IDX_MQ_PKG_LOG_ERROR_CODE ON dwh.mq_pkg_log(error_code);

-- =====================================================
-- DWH.EMPLOYEE
-- Core employee information.
-- =====================================================
create table dwh.employee (
    employee_id varchar2(16 char) primary key,
    ssn varchar2(9 char) unique not null,
    created_at        TIMESTAMP DEFAULT ON NULL SYSTIMESTAMP NOT NULL,
    created_by        VARCHAR2(100 CHAR) DEFAULT ON NULL USER NOT NULL,
    updated_at        TIMESTAMP DEFAULT ON NULL SYSTIMESTAMP NOT NULL,
    updated_by        VARCHAR2(100 CHAR) DEFAULT ON NULL USER NOT NULL
);

-- =====================================================
-- DWH.DEMOGRAPHIC
-- Core employee demographic information.
-- =====================================================
CREATE TABLE dwh.demographic (
  demographic_id    NUMBER GENERATED BY DEFAULT AS IDENTITY (START WITH 1000001) CONSTRAINT PK_DEMOGRAPHIC PRIMARY KEY,
  employee_id       VARCHAR2(16 CHAR) NOT NULL ENABLE NOVALIDATE CONSTRAINT FK_DEMOGRAPHIC_EMPLOYEE_ID REFERENCES dwh.employee(employee_id) ON DELETE CASCADE,
  first_name        VARCHAR2(64 CHAR),
  middle_name       VARCHAR2(64 CHAR),
  last_name         VARCHAR2(64 CHAR),
  prefix_id         NUMBER REFERENCES dwh.mq_lookup(lookup_id),
  suffix_id         NUMBER REFERENCES dwh.mq_lookup(lookup_id),
  birthdate         DATE,
  dt_of_death       DATE,
  gender_id         NUMBER REFERENCES dwh.mq_lookup(lookup_id),
  mar_status_id     NUMBER REFERENCES dwh.mq_lookup(lookup_id),
  email_addr        VARCHAR2(120 CHAR),
  deleted_status     NUMBER REFERENCES dwh.mq_lookup(lookup_id),
  created_at        TIMESTAMP DEFAULT ON NULL SYSTIMESTAMP NOT NULL,
  created_by        VARCHAR2(100 CHAR) DEFAULT ON NULL USER NOT NULL,
  updated_at        TIMESTAMP DEFAULT ON NULL SYSTIMESTAMP NOT NULL,
  updated_by        VARCHAR2(100 CHAR) DEFAULT ON NULL USER NOT NULL
);

-- =====================================================
-- DWH.ADDRESS
-- Member address information.
-- =====================================================
CREATE TABLE dwh.address (
  address_id      NUMBER GENERATED BY DEFAULT AS IDENTITY (START WITH 1000001) CONSTRAINT PK_ADDRESS PRIMARY KEY,
  demographic_id  NUMBER NOT NULL REFERENCES dwh.demographic(demographic_id) ON DELETE CASCADE,
  address1        VARCHAR2(64 CHAR),
  address2        VARCHAR2(64 CHAR),
  address3        VARCHAR2(64 CHAR),
  address4        VARCHAR2(64 CHAR),
  city            VARCHAR2(64 CHAR),
  county          VARCHAR2(64 CHAR),
  state           VARCHAR2(64 CHAR),
  postal          VARCHAR2(16 CHAR),
  country         VARCHAR2(16 CHAR),
  address_type_id NUMBER REFERENCES dwh.mq_lookup(lookup_id),
  created_at      TIMESTAMP DEFAULT ON NULL SYSTIMESTAMP NOT NULL,
  created_by      VARCHAR2(100 CHAR) DEFAULT ON NULL USER NOT NULL,
  updated_at      TIMESTAMP DEFAULT ON NULL SYSTIMESTAMP NOT NULL,
  updated_by      VARCHAR2(100 CHAR) DEFAULT ON NULL USER NOT NULL
);

-- =====================================================
-- DWH.PHONE
-- Member phone information.
-- =====================================================
CREATE TABLE dwh.phone (
  phone_id       NUMBER GENERATED BY DEFAULT AS IDENTITY (START WITH 1000001) CONSTRAINT PK_PHONE PRIMARY KEY,
  demographic_id NUMBER NOT NULL REFERENCES dwh.demographic(demographic_id) ON DELETE CASCADE,
  phone          VARCHAR2(32 CHAR),
  phone_ext      VARCHAR2(8 CHAR),
  phone_type_id  NUMBER REFERENCES dwh.mq_lookup(lookup_id),
  created_at     TIMESTAMP DEFAULT ON NULL SYSTIMESTAMP NOT NULL,
  created_by     VARCHAR2(100 CHAR) DEFAULT ON NULL USER NOT NULL,
  updated_at     TIMESTAMP DEFAULT ON NULL SYSTIMESTAMP NOT NULL,
  updated_by     VARCHAR2(100 CHAR) DEFAULT ON NULL USER NOT NULL
);

-- =====================================================
-- DWH.ACCOUNT
-- Organization accounts (ex. business units, subsidiaries, etc).
-- =====================================================
CREATE TABLE dwh.account (
  account_id       NUMBER GENERATED BY DEFAULT AS IDENTITY (START WITH 100501) CONSTRAINT PK_ACCOUNT PRIMARY KEY,
  account_code     VARCHAR2(64 CHAR) UNIQUE NOT NULL,
  account_name     VARCHAR2(128 CHAR) NOT NULL,
  account_status_id NUMBER REFERENCES dwh.mq_lookup(lookup_id),
  account_type_id   NUMBER REFERENCES dwh.mq_lookup(lookup_id) ,
  created_at      TIMESTAMP DEFAULT ON NULL SYSTIMESTAMP NOT NULL,
  created_by      VARCHAR2(100 CHAR) DEFAULT ON NULL USER NOT NULL,
  updated_at      TIMESTAMP DEFAULT ON NULL SYSTIMESTAMP NOT NULL,
  updated_by      VARCHAR2(100 CHAR) DEFAULT ON NULL USER NOT NULL
);

-- =====================================================
-- DWH.EMPLOYMENT
-- Member employment and sub-unit information.
-- =====================================================
CREATE TABLE dwh.employment (
  employment_id   NUMBER GENERATED BY DEFAULT AS IDENTITY (START WITH 1000001) CONSTRAINT PK_EMPLOYMENT PRIMARY KEY,
  demographic_id  NUMBER NOT NULL REFERENCES dwh.demographic(demographic_id) ON DELETE CASCADE,
  account_code    VARCHAR2(64 CHAR) NOT NULL REFERENCES dwh.account(account_code),
  hire_dt         DATE NOT NULL,
  termination_dt  DATE,
  created_at      TIMESTAMP DEFAULT ON NULL SYSTIMESTAMP NOT NULL,
  created_by      VARCHAR2(100 CHAR) DEFAULT ON NULL USER NOT NULL,
  updated_at      TIMESTAMP DEFAULT ON NULL SYSTIMESTAMP NOT NULL,
  updated_by      VARCHAR2(100 CHAR) DEFAULT ON NULL USER NOT NULL
);

-- =====================================================
-- DWH.CENSUS
-- Composite snapshot for nightly delta detection bypass.
-- =====================================================
CREATE TABLE dwh.census (
  census_id         NUMBER GENERATED BY DEFAULT AS IDENTITY (START WITH 1000001) CONSTRAINT PK_CENSUS PRIMARY KEY,
  demographic_id    NUMBER NOT NULL REFERENCES dwh.demographic(demographic_id) ON DELETE CASCADE,
  employee_id       VARCHAR2(16 CHAR) NOT NULL,
  first_name        VARCHAR2(64 CHAR),
  middle_name       VARCHAR2(64 CHAR),
  last_name         VARCHAR2(64 CHAR),
  birthdate         DATE,
  dt_of_death       DATE,
  hire_date         DATE,
  termination_date  DATE,
  account_code      VARCHAR2(32 CHAR),
  plan_status       VARCHAR2(64 CHAR),
  created_at        TIMESTAMP DEFAULT ON NULL SYSTIMESTAMP NOT NULL,
  created_by        VARCHAR2(100 CHAR) DEFAULT ON NULL USER NOT NULL,
  updated_at        TIMESTAMP DEFAULT ON NULL SYSTIMESTAMP NOT NULL,
  updated_by        VARCHAR2(100 CHAR) DEFAULT ON NULL USER NOT NULL
);
