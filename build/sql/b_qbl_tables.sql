-- =====================================================
-- QBL.QBL_USERS
-- =====================================================
CREATE TABLE qbl_users (
    qbl_user_id   NUMBER GENERATED BY DEFAULT AS IDENTITY CONSTRAINT PK_QBL_USERS PRIMARY KEY,
    username      VARCHAR2(100 CHAR) NOT NULL CONSTRAINT UQ_QBL_USERS_USERNAME UNIQUE,
    email         VARCHAR2(255 CHAR) NOT NULL CONSTRAINT UQ_QBL_USERS_EMAIL    UNIQUE,
    password_hash VARCHAR2(510 CHAR) NOT NULL,
    role_id       VARCHAR2(50 CHAR)  DEFAULT 'user' NOT NULL,
    is_active     NUMBER(1)          DEFAULT 0 CHECK (is_active IN (0, 1)),

    created_at TIMESTAMP DEFAULT SYSTIMESTAMP NOT NULL,
    created_by VARCHAR2(100 CHAR) DEFAULT 'SYSTEM' NOT NULL,
    updated_at TIMESTAMP DEFAULT SYSTIMESTAMP NOT NULL,
    updated_by VARCHAR2(100 CHAR) DEFAULT 'SYSTEM' NOT NULL
);

-- =====================================================
-- QBL.USER_SETTINGS
-- =====================================================
CREATE TABLE user_settings (
    user_settings_id NUMBER GENERATED BY DEFAULT AS IDENTITY CONSTRAINT PK_USER_SETTINGS PRIMARY KEY,
    qbl_user_id      NUMBER NOT NULL,
    setting_key      VARCHAR2(100 CHAR) NOT NULL,
    setting_value    VARCHAR2(4000 CHAR),

    created_at TIMESTAMP DEFAULT SYSTIMESTAMP NOT NULL,
    created_by VARCHAR2(100 CHAR) DEFAULT 'SYSTEM' NOT NULL,
    updated_at TIMESTAMP DEFAULT SYSTIMESTAMP NOT NULL,
    updated_by VARCHAR2(100 CHAR) DEFAULT 'SYSTEM' NOT NULL,

    CONSTRAINT UQ_USER_SETTINGS UNIQUE (qbl_user_id, setting_key)
);

-- =====================================================
-- QBL.TEAMS
-- =====================================================
CREATE TABLE teams (
    team_id   NUMBER GENERATED BY DEFAULT AS IDENTITY CONSTRAINT PK_TEAMS PRIMARY KEY,
    team_name VARCHAR2(255 CHAR) NOT NULL CONSTRAINT UQ_TEAMS_NAME UNIQUE,

    created_at TIMESTAMP DEFAULT SYSTIMESTAMP NOT NULL,
    created_by VARCHAR2(100 CHAR) DEFAULT 'SYSTEM' NOT NULL,
    updated_at TIMESTAMP DEFAULT SYSTIMESTAMP NOT NULL,
    updated_by VARCHAR2(100 CHAR) DEFAULT 'SYSTEM' NOT NULL
);

-- =====================================================
-- QBL.USER_TEAMS
-- =====================================================
CREATE TABLE user_teams (
    qbl_user_id NUMBER REFERENCES qbl_users(qbl_user_id) ON DELETE CASCADE,
    team_id     NUMBER REFERENCES teams(team_id)         ON DELETE CASCADE,
    user_role   VARCHAR2(50 CHAR) DEFAULT 'USER' CHECK (user_role IN ('USER', 'ADMIN')),

    created_at TIMESTAMP DEFAULT SYSTIMESTAMP NOT NULL,
    created_by VARCHAR2(100 CHAR) DEFAULT 'SYSTEM' NOT NULL,
    updated_at TIMESTAMP DEFAULT SYSTIMESTAMP NOT NULL,
    updated_by VARCHAR2(100 CHAR) DEFAULT 'SYSTEM' NOT NULL,

    CONSTRAINT PK_USER_TEAMS PRIMARY KEY (qbl_user_id, team_id)
);

-- =====================================================
-- QBL.SETTING_LOOKUP
-- =====================================================
CREATE TABLE setting_lookup (
    setting_lookup_id  NUMBER GENERATED BY DEFAULT AS IDENTITY CONSTRAINT PK_SETTING_LOOKUP PRIMARY KEY,
    setting_name       VARCHAR2(100 CHAR)  NOT NULL CONSTRAINT UQ_SETTING_LOOKUP_NAME UNIQUE,
    setting_label      VARCHAR2(255 CHAR)  NOT NULL,
    setting_description VARCHAR2(1000 CHAR),
    setting_type       VARCHAR2(50 CHAR)   CHECK (setting_type IN ('STRING', 'NUMBER', 'BOOLEAN', 'JSON')),
    setting_value      VARCHAR2(4000 CHAR) NOT NULL,

    created_at TIMESTAMP DEFAULT SYSTIMESTAMP NOT NULL,
    created_by VARCHAR2(100 CHAR) DEFAULT 'SYSTEM' NOT NULL,
    updated_at TIMESTAMP DEFAULT SYSTIMESTAMP NOT NULL,
    updated_by VARCHAR2(100 CHAR) DEFAULT 'SYSTEM' NOT NULL
);

-- =====================================================
-- QBL.USER_SESSION
-- =====================================================
CREATE TABLE user_session (
    user_session_id NUMBER GENERATED BY DEFAULT AS IDENTITY
                        CONSTRAINT PK_USER_SESSION PRIMARY KEY,

    qbl_user_id NUMBER REFERENCES qbl_users(qbl_user_id) ON DELETE CASCADE NOT NULL,
    token_hash  VARCHAR2(64 CHAR)  NOT NULL,
    issued_at   TIMESTAMP WITH TIME ZONE NOT NULL,
    expires_at  TIMESTAMP WITH TIME ZONE NOT NULL,
    is_active   NUMBER(1,0) DEFAULT 1 NOT NULL,
    ip_address  VARCHAR2(45 CHAR),
    user_agent  VARCHAR2(512 CHAR),
    session_data CLOB,

    created_at TIMESTAMP DEFAULT SYSTIMESTAMP NOT NULL,
    created_by VARCHAR2(100 CHAR) DEFAULT 'SYSTEM' NOT NULL,
    updated_at TIMESTAMP DEFAULT SYSTIMESTAMP NOT NULL,
    updated_by VARCHAR2(100 CHAR) DEFAULT 'SYSTEM' NOT NULL
);

CREATE INDEX IDX_USER_SESSION_USER_ID  ON user_session (qbl_user_id);
CREATE INDEX IDX_USER_SESSION_TOKEN    ON user_session (token_hash);
CREATE INDEX IDX_USER_SESSION_ACTIVE   ON user_session (is_active, expires_at);

-- =====================================================
-- QBL.USER_SIGN_IN
-- qbl_user_id is nullable so failed attempts for unknown usernames
-- can still be recorded (attempted_username captures the raw input).
-- =====================================================
CREATE TABLE user_sign_in (
    user_sign_in_id NUMBER GENERATED BY DEFAULT AS IDENTITY
                        CONSTRAINT PK_USER_SIGN_IN PRIMARY KEY,

    qbl_user_id       NUMBER REFERENCES qbl_users(qbl_user_id) ON DELETE CASCADE,
    attempted_username VARCHAR2(100 CHAR),
    ip_address         VARCHAR2(45 CHAR),
    user_agent         VARCHAR2(512 CHAR),
    success            NUMBER(1,0) DEFAULT 0 NOT NULL,
    failure_reason     VARCHAR2(256 CHAR),

    created_at TIMESTAMP DEFAULT SYSTIMESTAMP NOT NULL,
    created_by VARCHAR2(100 CHAR) DEFAULT 'SYSTEM' NOT NULL,
    updated_at TIMESTAMP DEFAULT SYSTIMESTAMP NOT NULL,
    updated_by VARCHAR2(100 CHAR) DEFAULT 'SYSTEM' NOT NULL
);

CREATE INDEX IDX_USER_SIGN_IN_USER_DATE ON user_sign_in (qbl_user_id, created_at);
CREATE INDEX IDX_USER_SIGN_IN_IP_DATE   ON user_sign_in (ip_address, created_at);

-- =====================================================
-- QBL.USER_REFRESH_TOKEN
-- =====================================================
CREATE TABLE user_refresh_token (
    user_refresh_token_id NUMBER GENERATED BY DEFAULT AS IDENTITY
                              CONSTRAINT PK_USER_REFRESH_TOKEN PRIMARY KEY,

    qbl_user_id       NUMBER REFERENCES qbl_users(qbl_user_id) ON DELETE CASCADE NOT NULL,
    token_hash        VARCHAR2(64 CHAR)  NOT NULL,
    parent_session_id NUMBER,
    issued_at         TIMESTAMP WITH TIME ZONE NOT NULL,
    expires_at        TIMESTAMP WITH TIME ZONE NOT NULL,
    is_active         NUMBER(1,0) DEFAULT 1 NOT NULL,
    ip_address        VARCHAR2(45 CHAR),
    user_agent        VARCHAR2(512 CHAR),

    created_at TIMESTAMP DEFAULT SYSTIMESTAMP NOT NULL,
    created_by VARCHAR2(100 CHAR) DEFAULT 'SYSTEM' NOT NULL,
    updated_at TIMESTAMP DEFAULT SYSTIMESTAMP NOT NULL,
    updated_by VARCHAR2(100 CHAR) DEFAULT 'SYSTEM' NOT NULL
);

CREATE INDEX IDX_USER_REFRESH_TOKEN_HASH    ON user_refresh_token (token_hash);
CREATE INDEX IDX_USER_REFRESH_TOKEN_USER_ID ON user_refresh_token (qbl_user_id);
CREATE INDEX IDX_USER_REFRESH_TOKEN_ACTIVE  ON user_refresh_token (is_active, expires_at);

COMMIT;
