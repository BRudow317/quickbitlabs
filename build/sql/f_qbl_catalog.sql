-- =====================================================
-- QBL.CATALOG_REGISTRY
-- Scalar Catalog fields as columns (for filtering/listing without JSON parsing).
-- List fields (entities, filters, joins, sort_columns, assignments) and
-- the properties dict are stored as JSON CLOBs alongside the scalar columns.
-- =====================================================
CREATE TABLE catalog_registry (
    catalog_registry_id NUMBER GENERATED BY DEFAULT AS IDENTITY
                             CONSTRAINT PK_CATALOG_REGISTRY PRIMARY KEY,

    -- Ownership
    scope         VARCHAR2(20 CHAR) DEFAULT 'USER' NOT NULL
                      CHECK (scope IN ('SYSTEM', 'TEAM', 'USER')),
    owner_user_id NUMBER REFERENCES qbl_users(qbl_user_id) ON DELETE CASCADE,
    team_id       NUMBER REFERENCES teams(team_id)          ON DELETE CASCADE,

    -- Scalar Catalog fields
    catalog_id      VARCHAR2(36 CHAR),
    name            VARCHAR2(255 CHAR) NOT NULL,
    alias           VARCHAR2(255 CHAR),
    label           VARCHAR2(255 CHAR),
    namespace       VARCHAR2(255 CHAR),
    catalog_version NUMBER        DEFAULT 1 NOT NULL,
    description     VARCHAR2(1000 CHAR),
    source_type     VARCHAR2(50 CHAR),
    catalog_limit   NUMBER,
    catalog_offset  NUMBER,

    -- JSON fields
    entities    CLOB CONSTRAINT chk_catalog_entities_json    CHECK (entities    IS JSON),
    filters     CLOB CONSTRAINT chk_catalog_filters_json     CHECK (filters     IS JSON),
    joins       CLOB CONSTRAINT chk_catalog_joins_json       CHECK (joins       IS JSON),
    sort_columns CLOB CONSTRAINT chk_catalog_sort_columns_json CHECK (sort_columns IS JSON),
    assignments CLOB CONSTRAINT chk_catalog_assignments_json CHECK (assignments IS JSON),
    properties  CLOB CONSTRAINT chk_catalog_properties_json  CHECK (properties  IS JSON),

    created_at TIMESTAMP DEFAULT SYSTIMESTAMP NOT NULL,
    created_by VARCHAR2(100 CHAR) DEFAULT 'SYSTEM' NOT NULL,
    updated_at TIMESTAMP DEFAULT SYSTIMESTAMP NOT NULL,
    updated_by VARCHAR2(100 CHAR) DEFAULT 'SYSTEM' NOT NULL,

    CONSTRAINT UQ_CATALOG_REGISTRY UNIQUE (scope, owner_user_id, name)
);

CREATE INDEX IDX_CATALOG_REGISTRY_SCOPE  ON catalog_registry (scope);
CREATE INDEX IDX_CATALOG_REGISTRY_USER   ON catalog_registry (owner_user_id);
CREATE INDEX IDX_CATALOG_REGISTRY_SOURCE ON catalog_registry (source_type);

-- =====================================================
-- QBL.CATALOG_SHARES
-- =====================================================
CREATE TABLE catalog_shares (
    share_id    NUMBER GENERATED BY DEFAULT AS IDENTITY CONSTRAINT PK_CATALOG_SHARES PRIMARY KEY,
    catalog_id  NUMBER REFERENCES catalog_registry(catalog_registry_id) ON DELETE CASCADE,

    shared_user_id NUMBER REFERENCES qbl_users(qbl_user_id) ON DELETE CASCADE,
    shared_team_id NUMBER REFERENCES teams(team_id)          ON DELETE CASCADE,

    permission_level VARCHAR2(50 CHAR) CHECK (permission_level IN ('VIEWER', 'EDITOR')),

    created_at TIMESTAMP DEFAULT SYSTIMESTAMP NOT NULL,
    created_by VARCHAR2(100 CHAR) DEFAULT 'SYSTEM' NOT NULL,
    updated_at TIMESTAMP DEFAULT SYSTIMESTAMP NOT NULL,
    updated_by VARCHAR2(100 CHAR) DEFAULT 'SYSTEM' NOT NULL
);
